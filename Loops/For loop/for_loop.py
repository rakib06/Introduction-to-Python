# for each number i in range 0-4. range(5) function returns list [0, 1, 2, 3, 4]
for i in range(5):
    # this line is executed 5 times. First time i equals 0, then 1, ...
    print(i)

# create new list
primes = [2, 3, 5, 7]

# iterate over primes using for loop
for prime in primes:
    print(prime)
